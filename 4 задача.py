print("Давайте расчитаем площадь боковой поверхности и объем прямоугольного параллелепипида:")
h=input("Введите длину бокового ребра (см): ")
a=input("Введите длину первой стороны основания (см): ")
b=input("Введите длину второй стороны основания (см): ")
print("Площадь боковой поверхности параллелепипида =",((int(a)+int(b))*2)*int(h),"см2")
print("Объем прямоугольного параллелепипида =", (int(a)*int(b))*int(h), "см3")